const dictionaries = { id, en, zh };
interface Value {
  locale: AppLocale;
  setLocale: (locale: AppLocale) => void;
  t: (key: Key) => string;
  l: (idText: string, enText: string, zhText: string) => string;
}
const Context = createContext<Value | null>(null);
export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<AppLocale>(
    () => (safeStorage.getItem("pli-locale") as AppLocale | null) ?? "id",
  );
  setFormattingLocale(locale);
  useEffect(() => {
    document.documentElement.lang = locale === "zh" ? "zh-CN" : locale;
  }, [locale]);
  const setLocale = (next: AppLocale) => {
    setLocaleState(next);
    safeStorage.setItem("pli-locale", next);
  };
  const value = useMemo<Value>(
    () => ({
      locale,
      setLocale,
      t: (key) => dictionaries[locale][key],
      l: (idText, enText, zhText) => (locale === "id" ? idText : locale === "en" ? enText : zhText),
    }),
    [locale],
  );
  return <Context value={value}>{children}</Context>;
}
export function useI18n() {
  const value = useContext(Context);
  if (value === null) throw new Error("useI18n must be inside I18nProvider");
  return value;
}
